class Book {
    constructor(isbn, title, author, lang, press,
                date, intro, cover, price, stock) {
        this.isbn = isbn;
        this.title = title;
        this.author = author;
        this.lang = lang;
        this.press = press;
        this.date = date;
        this.intro = intro;
        this.cover = cover;
        this.price = price;
        this.stock = stock;
    }
}

export default Book;
